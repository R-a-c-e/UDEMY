console.log('The following is about switch statements!');
let dayOfWeek = 'Tuesday';

switch(dayOfWeek){
    case 'Monday':
        console.log("Monday");
        break;
    case 'Tuesday':
        console.log("Tuesday");
        break;
    case 'Wednesday':
        console.log("Wednesday");
        break;
    case 'Thursday':
        console.log("Thursday");
        break;
    case 'Friday':
        console.log("Friday");
        break;
    case 'Saturday':
        console.log("Saturday");
        break;
    case 'Sunday':
        console.log("Sunday");
        break;
    default:
        console.log("what?");
        break;

}
